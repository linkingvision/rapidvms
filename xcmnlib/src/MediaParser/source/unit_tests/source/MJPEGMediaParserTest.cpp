void MJPEGMediaParserTest::TestTypeCheck()
{
    printf("MJPEGMediaParserTest::TestTypeCheck()\n");
    fflush(stdout);

    CPPUNIT_ASSERT_NO_THROW( MJPEGMediaParser(jpeg, jpegLen) );

    MJPEGMediaParser mjpeg(jpeg, jpegLen);
    CPPUNIT_ASSERT( mjpeg.IsYourType() );
}

void MJPEGMediaParserTest::TestParse()
{
    printf("MJPEGMediaParserTest::TestParse()\n");
    fflush(stdout);

    {
        MJPEGMediaParser mjpeg(jpeg, jpegLen);
        MJPEGInfo info;
        CPPUNIT_ASSERT( mjpeg.Parse(info) );
        CPPUNIT_ASSERT( info.ContainsKeyFrame() );
        CPPUNIT_ASSERT( info.GetFrameType() == FRAME_TYPE_I_FRAME );
        CPPUNIT_ASSERT( info.GetMediaType() == MEDIA_TYPE_MJPEG );
        CPPUNIT_ASSERT( info.GetChromaFormat() == CHROMA_FORMAT_YUV_420 );
        CPPUNIT_ASSERT( info.GetFrameWidth() == 250 );
        CPPUNIT_ASSERT( info.GetFrameHeight() == 250 );
    }
    {
        MJPEGMediaParser mjpeg(jpeg, jpegLen);
        MediaInfo info;
        CPPUNIT_ASSERT( mjpeg.Parse(info) );
        CPPUNIT_ASSERT( info.ContainsKeyFrame() );
        CPPUNIT_ASSERT( info.GetFrameType() == FRAME_TYPE_I_FRAME );
        CPPUNIT_ASSERT( info.GetMediaType() == MEDIA_TYPE_MJPEG );
        CPPUNIT_ASSERT( info.GetChromaFormat() == CHROMA_FORMAT_YUV_420 );
        CPPUNIT_ASSERT( info.GetFrameWidth() == 250 );
        CPPUNIT_ASSERT( info.GetFrameHeight() == 250 );
    }
}
